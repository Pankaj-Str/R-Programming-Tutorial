# Integer Variable
count <- as.integer(42)

# Arithmetic Operations
num1 <- 20L  # 'L' suffix indicates an integer literal
num2 <- 10L

# Addition
sum_result <- num1 + num2

# Subtraction
diff_result <- num1 - num2

# Multiplication
prod_result <- num1 * num2

# Division
div_result <- num1 / num2

# Exponentiation
exp_result <- num1^2

# Modulus (remainder)
mod_result <- num1 %% num2

# Print the results
cat("Integer Variable (count):", count, "\n")
cat("Addition:", sum_result, "\n")
cat("Subtraction:", diff_result, "\n")
cat("Multiplication:", prod_result, "\n")
cat("Division:", div_result, "\n")
cat("Exponentiation:", exp_result, "\n")
cat("Modulus (remainder):", mod_result, "\n")
