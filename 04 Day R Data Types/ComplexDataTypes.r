# Complex Variable
complex_num <- 3 + 2i

# Accessing Real and Imaginary Parts
real_part <- Re(complex_num)
imaginary_part <- Im(complex_num)

# Complex Arithmetic Operations
another_complex_num <- 1 - 4i

# Addition
sum_complex <- complex_num + another_complex_num

# Subtraction
diff_complex <- complex_num - another_complex_num

# Multiplication
prod_complex <- complex_num * another_complex_num

# Division
div_complex <- complex_num / another_complex_num

# Print the results
cat("Complex Variable (complex_num):", complex_num, "\n")
cat("Real Part:", real_part, "\n")
cat("Imaginary Part:", imaginary_part, "\n")
cat("Complex Addition:", sum_complex, "\n")
cat("Complex Subtraction:", diff_complex, "\n")
cat("Complex Multiplication:", prod_complex, "\n")
cat("Complex Division:", div_complex, "\n")
