# Logical Variables
is_student <- TRUE
is_adult <- FALSE

# Logical Operations
logical_and <- is_student & is_adult
logical_or <- is_student | is_adult
logical_not_student <- !is_student

# Print the results
cat("Is Student:", is_student, "\n")
cat("Is Adult:", is_adult, "\n")
cat("Logical AND (Student AND Adult):", logical_and, "\n")
cat("Logical OR (Student OR Adult):", logical_or, "\n")
cat("Logical NOT (NOT Student):", logical_not_student, "\n")
