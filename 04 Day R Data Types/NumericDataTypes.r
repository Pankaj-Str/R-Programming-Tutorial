# Integer
x <- 42

# Double (floating-point)
y <- 3.1415

# Arithmetic operations
a <- 10
b <- 5

# Addition
sum_result <- a + b

# Subtraction
diff_result <- a - b

# Multiplication
prod_result <- a * b

# Division
div_result <- a / b

# Exponentiation
exp_result <- a^b

# Modulus (remainder)
mod_result <- a %% b

# Print the results
cat("Integer (x):", x, "\n")
cat("Double (y):", y, "\n")
cat("Sum:", sum_result, "\n")
cat("Difference:", diff_result, "\n")
cat("Product:", prod_result, "\n")
cat("Division:", div_result, "\n")
cat("Exponentiation:", exp_result, "\n")
cat("Modulus (remainder):", mod_result, "\n")
