# Assignment Operators Example

# Numeric Variables
x <- 10
y <- 3

# Assignment Operator (<-)
sum_result <- x + y

# Addition Assignment (+=)
x <- x + y

# Subtraction Assignment (-=)
y <- y - 2

# Multiplication Assignment (*=)
x <- x * 2

# Division Assignment (/=)
y <- y / 2

# Print the results
cat("Using Assignment Operator (<-):\n")
cat("sum_result:", sum_result, "\n")

cat("\nUsing Addition Assignment (+=):\n")
cat("x:", x, "\n")

cat("\nUsing Subtraction Assignment (-=):\n")
cat("y:", y, "\n")

cat("\nUsing Multiplication Assignment (*=):\n")
cat("x:", x, "\n")

cat("\nUsing Division Assignment (/=):\n")
cat("y:", y, "\n")
