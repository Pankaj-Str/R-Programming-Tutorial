# Comparison Operators Example

# Numeric Variables
x <- 10
y <- 3

# Equal to
is_equal <- x == y

# Not equal to
is_not_equal <- x != y

# Less than
is_less_than <- x < y

# Greater than
is_greater_than <- x > y

# Less than or equal to
is_less_than_or_equal <- x <= y

# Greater than or equal to
is_greater_than_or_equal <- x >= y

# Print the results
cat("Equal to (x == y):", is_equal, "\n")
cat("Not equal to (x != y):", is_not_equal, "\n")
cat("Less than (x < y):", is_less_than, "\n")
cat("Greater than (x > y):", is_greater_than, "\n")
cat("Less than or equal to (x <= y):", is_less_than_or_equal, "\n")
cat("Greater than or equal to (x >= y):", is_greater_than_or_equal, "\n")
