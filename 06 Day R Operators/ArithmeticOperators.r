# Arithmetic Operators Example

# Numeric Variables
x <- 10
y <- 3

# Addition
sum_result <- x + y

# Subtraction
difference_result <- x - y

# Multiplication
product_result <- x * y

# Division
quotient_result <- x / y

# Exponentiation
exponent_result <- x^y

# Modulus (Remainder)
remainder_result <- x %% y

# Integer Division (Quotient)
integer_division_result <- x %/% y

# Print the results
cat("Addition (x + y):", sum_result, "\n")
cat("Subtraction (x - y):", difference_result, "\n")
cat("Multiplication (x * y):", product_result, "\n")
cat("Division (x / y):", quotient_result, "\n")
cat("Exponentiation (x ^ y):", exponent_result, "\n")
cat("Modulus (x %% y):", remainder_result, "\n")
cat("Integer Division (x %/% y):", integer_division_result, "\n")
